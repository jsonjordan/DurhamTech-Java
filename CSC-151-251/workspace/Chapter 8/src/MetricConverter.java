/*
 * Example of using a static method
 */
public class MetricConverter {

	public static double milesToKilos( double miles ){
		return miles * 1.609;
	}// end milesToKilos
	
	public static double kilosToMiles( double kilos ){
		return kilos / 1.609;
	}// end kilosToMiles
	
	
	
	
} // end class
