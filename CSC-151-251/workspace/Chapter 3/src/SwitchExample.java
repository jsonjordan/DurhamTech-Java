public class SwitchExample {

	public static void main(String[] args) {
		// declare vars
		int grade = 88;
		
		switch (grade) {
		
		case 88:
			System.out.print("You have a B grade");
			break;
			
		case 99:
			System.out.print("You have an A grade");
			break;
		} // end switch
		
		
		
		
		
		
	} // end main

} // end class
