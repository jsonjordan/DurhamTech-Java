// for (init;condition;increment)


public class ForLoopExample {

	public static void main(String[] args) {
		
		
		for (int number = 1; number <= 10; number ++){
			System.out.println(number + "\t" + number*number);
			
			
			
			
			
		} // end for loop
		
		
		
		

	}

}
